# computer_operations.py - datora funkcijas

def power_on(computer):
    print(f"Ieslēdz {computer['Ražotājs']} {computer['Modelis']} datoru.")

def shut_down(computer):
    print(f"Izslēdz {computer['Ražotājs']} {computer['Modelis']} datoru.")

def reboot(computer):
    print(f"Restartē {computer['Ražotājs']} {computer['Modelis']} datoru.")
